# var Declaration
emp1 = 5
emp2 = 10
emp3 = 15
emp4 = 20
emp5 = 25

# Calculation
total_sales = emp1 + emp2 + emp3 + emp4 + emp5

average_sales = total_sales/5

highest_sales = max(emp1, emp2, emp3, emp4, emp5)

# Print results
print(f"The total sales is: {total_sales}")
print(f"The average sales is: {average_sales}")
print(f"The highest sales is: {highest_sales}")